--Seleccionar los artículos con sus proveedores:
SELECT ARTICULOS.*, SUPLIDORES.NOMBRE AS PROVEEDOR
FROM ARTICULOS
LEFT JOIN SUPLIDORES ON ARTICULOS.SUPLIDOR_ID = SUPLIDORES.ID;

--Contar el número de artículos suministrados por cada proveedor:
SELECT SUPLIDORES.NOMBRE AS PROVEEDOR, COUNT(ARTICULOS.ID) AS NUMERO_DE_ARTICULOS
FROM SUPLIDORES
LEFT JOIN ARTICULOS ON SUPLIDORES.ID = ARTICULOS.SUPLIDOR_ID
GROUP BY SUPLIDORES.NOMBRE;

--Listar los proveedores con sus artículos en stock:
SELECT SUPLIDORES.NOMBRE AS PROVEEDOR, ARTICULOS.*
FROM SUPLIDORES
INNER JOIN ARTICULOS ON SUPLIDORES.ID = ARTICULOS.SUPLIDOR_ID
WHERE ARTICULOS.STOCK > 0;

--Encontrar los proveedores sin artículos en stock:
SELECT SUPLIDORES.NOMBRE AS PROVEEDOR
FROM SUPLIDORES
LEFT JOIN ARTICULOS ON SUPLIDORES.ID = ARTICULOS.SUPLIDOR_ID
WHERE ARTICULOS.SUPLIDOR_ID IS NULL OR ARTICULOS.STOCK = 0;

--Seleccionar los proveedores y sus detalles de contacto:
SELECT SUPLIDORES.*, ARTICULOS.*
FROM SUPLIDORES
LEFT JOIN ARTICULOS 
ON SUPLIDORES.ID = ARTICULOS.SUPLIDOR_ID;

--Encontrar los artículos con el precio más bajo y su proveedor:
SELECT SUPLIDORES.NOMBRE AS PROVEEDOR, ARTICULOS.*
FROM ARTICULOS
INNER JOIN SUPLIDORES ON ARTICULOS.SUPLIDOR_ID = SUPLIDORES.ID
WHERE ARTICULOS.PRECIO = (
    SELECT MIN(PRECIO) 
    FROM ARTICULOS
);
